-- Database and Schema Setup


-- 1. Create and select the database
DROP DATABASE IF EXISTS pressstart_db;
CREATE DATABASE pressstart_db;
USE pressstart_db;

-- 2. Create Games Table
CREATE TABLE games (
    game_id INT AUTO_INCREMENT PRIMARY KEY,
    title VARCHAR(100) NOT NULL,
    genre VARCHAR(50),
    release_year INT,
    price_per_day DECIMAL(4,2),
    stock_count INT
);

-- 3. Create Members Table
CREATE TABLE members (
    member_id INT AUTO_INCREMENT PRIMARY KEY,
    username VARCHAR(50) NOT NULL,
    membership_type VARCHAR(20) DEFAULT 'Standard',
    join_date DATE
);

-- 4. Create Rentals Table
CREATE TABLE rentals (
    rental_id INT AUTO_INCREMENT PRIMARY KEY,
    member_id INT,
    game_id INT,
    rental_date DATE,
    days_rented INT,
    return_status VARCHAR(20), -- 'Returned' or 'Overdue'
    FOREIGN KEY (member_id) REFERENCES members(member_id),
    FOREIGN KEY (game_id) REFERENCES games(game_id)
);


