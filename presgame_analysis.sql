-- 10 New Practice Queries
-- 1.Find all games that have "Mario" anywhere in their title.

SELECT title, genre, release_year 
FROM games 
WHERE title LIKE '%Mario%';

-- 2.Find the top 3 most expensive games to rent per day.
SELECT title, price_per_day 
FROM games 
ORDER BY price_per_day DESC 
LIMIT 3;

-- 3.Find all games where the stock is completely empty.
SELECT title, genre 
FROM games 
WHERE stock_count = 0;

-- 4.Classify games into "Retro" or "Modern" based on their release year.

SELECT title, release_year,
       CASE 
           WHEN release_year < 2020 THEN 'Retro/Classic'
           ELSE 'Modern Era'
       END AS game_category
FROM games;

-- 5.Group rentals by game and show only the games that have been rented more than once. (Note: WHERE filters rows before grouping; HAVING filters after grouping!)

SELECT game_id, COUNT(rental_id) AS rental_count
FROM rentals
GROUP BY game_id
HAVING rental_count > 1;

-- 6.Find the average rental duration (days rented) for each unique return status.
SELECT return_status, AVG(days_rented) AS avg_days_kept
FROM rentals
GROUP BY return_status;

-- 7.Count how many unique games have actually been rented out at least once.
SELECT COUNT(DISTINCT game_id) AS unique_games_rented 
FROM rentals;

-- 8.Find usernames of members who currently hold an "Overdue" game rental.

SELECT username, membership_type 
FROM members 
WHERE member_id IN (SELECT member_id FROM rentals WHERE return_status = 'Overdue');

-- 9.Display a history list showing the game titles alongside the dates they were checked out.
SELECT r.rental_date, g.title, r.return_status
FROM rentals r
INNER JOIN games g ON r.game_id = g.game_id
ORDER BY r.rental_date DESC;

-- 10.Calculate the total money generated by each member by multiplying their days_rented by the game's price_per_day
SELECT m.username, 
       SUM(r.days_rented * g.price_per_day) AS total_spent
FROM rentals r
INNER JOIN members m ON r.member_id = m.member_id
INNER JOIN games g ON r.game_id = g.game_id
GROUP BY m.username
ORDER BY total_spent DESC;
